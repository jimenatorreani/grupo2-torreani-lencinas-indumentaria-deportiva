<p>La metodología de venta en la empresa de manera general es la siguiente:  Cuando el usuario ya registrado en el sitio navega por la página buscando algún producto en concreto y lo carga en un carrito. Automáticamente el sistema le pedirá datos respecto a la compra tales como  cantidad, talle, colores, formas de pago y preferencias de envío. En caso de no estar registrado, el sistema le pedirá previamente que se registre con los datos pertinentes.
