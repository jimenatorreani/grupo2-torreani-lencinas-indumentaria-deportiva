<h2>Quiénes Somos</h2>

<p>
Somos una empresa dedicada a la comercialización de indumentaria deportiva, creada y fundada en el año 2020 por Torreani Jimena y Lencinas Mauricio.
Uno de los principales motivos que tuvimos al crear esta empresa es compartir y empatizar con las personas que realizan distintos tipos de deportes y necesitan comodidad, calidad y confort.
