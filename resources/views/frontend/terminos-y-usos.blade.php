<h3>Políticas de Privacidad</h3>

<p>La empresa es consciente de la preocupación de los clientes ante la seguridad de los datos ingresados asimismo se compromete con el resguardo y protección de los datos a fin de que el cliente pueda navegar, cargar y comprar de manera segura y confiable.


